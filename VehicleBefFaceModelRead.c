HaarClassifierCascade* VehicleBefFaceModelRead()
{
	OSAL_INT32 i                 = 0;
	OSAL_INT32 stage_size        = 0;
	OSAL_INT32 classifier_size   = 0;
	OSAL_INT32 classifier_offset = 0;

	OSAL_INT32 haar_feature_offset     = 0;
	OSAL_INT32 classifier_left_offset  = 0; 
	OSAL_INT32 classifier_right_offset = 0;

	HaarClassifierCascade* cascade    = OSAL_NULL;
	HaarStageClassifier*   stages     = OSAL_NULL;
	HaarClassifier*        classifier = OSAL_NULL;
		
	cascade = (HaarClassifierCascade*)HaarClassifierCascade_vehicleBefface;

	/* load stages */
	stage_size = StageClassifier_vehicleBefface[0];

	stages = (HaarStageClassifier *)(StageClassifier_vehicleBefface + 1);

	/* load stages float threshold */
	for(i = 0; i < stage_size; ++i)
	{
		(stages + i)->threshold = StageClassifier_threshold_vehicleBefface[i];
	}

	/* load classifier */
	classifier_size = Classifier_vehicleBefface[0];

	classifier = (HaarClassifier*)(Classifier_vehicleBefface + 1);

	/* link cascade with stages */
	cascade->stage_classifier = stages;

	/* link stages with classifiers */
	classifier_offset = 0;
	for(i = 0; i < stage_size; ++i)
	{
		(stages + i)->classifier = (classifier + classifier_offset);
		classifier_offset +=(stages + i)->count;
	}

	/* link classifiers with haar_featrue/left/right */
	haar_feature_offset     = 0;
	classifier_left_offset  = sizeof(HaarFeature)/sizeof(OSAL_INT32) + 1;  /* + 1 is padding threshold */
	classifier_right_offset = classifier_left_offset + 1;

	for(i = 0; i < classifier_size; ++i)
	{
		(classifier + i)->haar_feature = (HaarFeature*)(Haar_feature_vehicleBefface + haar_feature_offset);
		(classifier + i)->left         = (Haar_feature_vehicleBefface + classifier_left_offset);
		(classifier + i)->right        = (Haar_feature_vehicleBefface + classifier_right_offset);

		haar_feature_offset     += 20;   /* sizeof(HaarFeature)/sizeof(int) + 4, padding threshold/left/right/alpha */
		classifier_left_offset  += 20;
		classifier_right_offset += 20;

		/* link classifiers with threshold/alpha */
		(classifier + i)->threshold = (float*)Classifier_threshold_vehicleBefface + i;
		(classifier + i)->alpha     = (float*)Classifier_alpha_vehicleBefface + 2 * i;

		/* load rect weight in haar feature */
		(classifier + i)->haar_feature->rect[0].weight = Haar_feature_rect_weight_vehicleBefface[3 * i];
		(classifier + i)->haar_feature->rect[1].weight = Haar_feature_rect_weight_vehicleBefface[3 * i + 1];
		(classifier + i)->haar_feature->rect[2].weight = Haar_feature_rect_weight_vehicleBefface[3 * i + 2];
	}
	
	return cascade;
}
